package dev.quinnlane.cloudy.common.unit;

/**
 * The UnitLocales enum defines different systems of measurement
 * standards that can be used in an application.
 * <p>
 * It includes the following values:
 * <ul>
 *     <li>SI: Represents the International System of Units (Système International d'Unités), which is the modern metric system.</li>
 *     <li>CUSTOMARY: Represents customary units commonly used in the United States and select other places (e.g., imperial units).</li>
 *     <li>MIXED: Represents a combination or blend of SI and customary units, commonly used in the United Kingdom and Canada.</li>
 * </ul>
 */
public enum UnitLocales {
	SI, CUSTOMARY, MIXED
}
